package a4_method;

public class Example3 {
    public static void main(String[] args) {
        // 숫자를 제곱하는 메서드
        System.out.println(square(4)); // 16
        System.out.println(square(9)); // 81
        System.out.println(square(15)); // 225
    }
    public static int square(int num) {
        // 코드를 작성하여 매개변수의 제곱을 반환하시오
        return num * num;
    }
}
